
<h3>¿Como diablos funciona esto?</h3>
<p>Tomamos los últimos titulares (mediante RSS) de los diarios (y otras fuentes) Chilenos (LaTercera, Emol, etc.) y calculamos de qué político se está hablando más (y lo mostramos).</p>
<br />
<h3>¿Mis datos quedan guardados al momento de calificar a un político?</h3>
<p>Solo la nota que le pusiste y la fecha (para efectos estadísticos).</p>
<br />
<h3>¿Por qué algunas veces sale siempre el mismo político?</h3>
<p>Bueno, eso hay que preguntarselo a los diarios!, de todas formas, espérate en época de elecciones (van a salir todos).</p>
